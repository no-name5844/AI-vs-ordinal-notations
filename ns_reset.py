"""
NS 超越 Veblen: Γ₀, SVO, LVO 及复位规律

关键发现: 许多高层序数在 f 计算中"复位"到 f=4!

原因: 这些序数的基本列 [2] 追溯到 ε₀，而 f(ε₀)=4。

已验证的复位点:
  ε₀:     ε₀[2]=ω, f=4
  Γ₀:     Γ₀[2]=ε₀, f(Γ₀)=f(ε₀)=4  ← 再次复位!

Γ₀ = φ(1,0,0): Γ₀[n+1]=φ(Γ₀[n],0), Γ₀[0]=0
  Γ₀[1]=1, Γ₀[2]=ε₀
  f(Γ₀) = f(ε₀) = 4

SVO = lim{ε₀, Γ₀, φ(1,0,0,0), ...}
  SVO[1]=ε₀, SVO[2]=Γ₀
  f(SVO) = f(Γ₀) = f(ε₀) = 4  ← 再次复位!

每个"极限的最大层Veblen"都复位到 f=4。
每次复位后贡献 = 8/9。

这意味着 S 可能发散! 因为有无穷多复位点。
"""

from fractions import Fraction

print("=" * 70)
print("NS: 复位规律与发散问题")
print("=" * 70)

# 复位点列表
reset_points = [
    ("ε₀ = φ(1,0)",      "ω",         4,  "8/9"),
    ("Γ₀ = φ(1,0,0)",    "ε₀",        4,  "8/9"),
    ("SVO",               "Γ₀",        4,  "8/9"),
    ("LVO",               "SVO",       4,  "8/9 (?)"),
    ("BHO",               "LVO",       4,  "8/9 (?)"),
]

print(f"\n{'复位点':<22s} {'[2]':<12s} {'f值':<8s} {'贡献':<10s}")
print("-" * 55)
for name, target, fv, contrib in reset_points:
    print(f"{name:<22s} {target:<12s} {fv:<8d} {contrib:<10s}")

print(f"""
══════════════════════════════════════════════════════════════
  复位级联: S 的发散问题
══════════════════════════════════════════════════════════════

每个复位点贡献 8/9。有无穷多复位点意味着:

  S 可能 → ∞ ??

但 S 定义在全部序数上——它真的发散吗?
还是 S 有上界 (比如某个固定点)?

关键: 复位点之间的距离越来越大 (ε₀ < Γ₀ < SVO < LVO < BHO < ...)
如果复位频率足够低，级联可能收敛。

目前已知:
  ω 层:           贡献 ≈ 0.844  (非等比)
  ε₀ 复位:        贡献 = 8/9    (f=4)
  Veblen 非复位层: 贡献 = 1/18 + 1/4608 + ... ≈ 0.056  (f>4)
  Γ₀ 复位:        贡献 = 8/9    (f=4)
  SVO 复位:        贡献 = 8/9    (f=4)
  ...

如果每个"层次极限"都复位 f=4:
  S = 1 + 0.844 + (8/9)·(复位次数) + Σ 小贡献

  这分明发散!

但可能并非所有层次极限都复位。
例如: 某些序数层次的基本列 [2] 不是 ε₀。

这需要进一步验证每个序数的具体基本列。
""")

# 已确认的累积值
S_ww = Fraction(1, 1)
for k in range(1, 6):
    S_ww += Fraction(8, 3 * 2**(2**k))

S_after_e = S_ww + Fraction(8, 9)
S_after_v = S_after_e + Fraction(1, 18) + Fraction(1, 4608)
S_after_G = S_after_v + Fraction(8, 9)

print(f"\n=== 累积 S (确认+推测) ===")
print(f"S(ω^ω)           ≈ {float(S_ww):.6f}")
print(f"+ε₀ 复位 (8/9)    ≈ {float(S_after_e):.6f}")
print(f"+Veblen 非复位     ≈ {float(S_after_v):.6f}")
print(f"+Γ₀ 复位 (8/9)    ≈ {float(S_after_G):.6f}")
print(f"+SVO 复位 (8/9)   ≈ {float(S_after_G + Fraction(8,9)):.6f}")
print(f"+LVO 复位 (如果)   ≈ {float(S_after_G + Fraction(16,9)):.6f}")
