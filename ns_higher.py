"""
NS 分数展开 — 续篇：高层结构分析

已确认规律：
  S(1) = 0
  S(ω)  = 1   = lim (1/2 + 1/4 + 1/8 + ...)
  S(ω·2) = 1+1/2
  S(ω·3) = 1+1/2+1/8
  S(ω·4) = 1+1/2+1/8+1/32
  S(ω²)  = 1 + 1/2 + 1/8 + 1/32 + 1/128 + ... = 5/3

规律:
  从 ω·k 到 ω·(k+1) 的收敛增量 = 2/f(ω·k)
  f(ω·k) = 4^k
  增量 = 2/4^k
  
  S(ω·n) = 1 + Σ_{k=1}^{n-1} 2/4^k
  S(ω²) = 1 + 2/3 = 5/3

现在继续:
  从 ω²+ω·k 到 ω²+ω·(k+1):
  f(ω²+ω·k) = f(ω²)·4^k = 16·4^k = 4^{k+2}
  增量 = 2/4^{k+2}

  S(ω²·2) = S(ω²+ω) + Σ_{k=1}^{∞} 2/4^{k+2}
  其中 S(ω²+ω) = S(ω²) + 2/f(ω²) = 5/3 + 2/16 = 5/3 + 1/8 = 43/24
  S(ω²·2) = 43/24 + (2/64)/(3/4) = 43/24 + 1/24 = 44/24 = 11/6

更高层:
  f(ω^k) = 4^k (猜想可证)
  
  从 ω^k·m 到 ω^k·(m+1):
  第一子块收敛: 2/f(ω^k·m)
  f(ω^k·m) = f(ω^k)·4^{m-1} = 4^{k+m-1}

  S(ω^k) = 1 + Σ_{j=1}^{k-1} [ω^j 层的总贡献]
  
  ω^j 层的总贡献 = Σ_{m=1}^{∞} 2/4^{j+m-1} = (2/4^j)/(1-1/4) = (2/4^j)·(4/3) = 8/(3·4^j)

  所以 S(ω^k) = 1 + Σ_{j=1}^{k-1} 8/(3·4^j)
              = 1 + (8/3)·(1/4)(1-1/4^{k-1})/(1-1/4)
              = 1 + (8/3)·(1/4)·(4/3)·(1-1/4^{k-1})
              = 1 + (8/9)·(1-1/4^{k-1})

  S(ω^ω) = lim_{k→∞} S(ω^k) = 1 + 8/9 = 17/9 ≈ 1.889

然后 f(ω^ω) = f(ω²) = 16
从 ω^ω 之后又有新的块...

总结: S(α) 始终在 [0, 2) 内。
  S(ω^ω) = 17/9 仍小于 2
"""

from fractions import Fraction

def S_omega_pow(k: int) -> Fraction:
    """计算 S(ω^k)"""
    # S(ω^k) = 1 + (8/9)(1 - 1/4^{k-1})
    if k <= 0:
        return Fraction(0, 1)
    return Fraction(1, 1) + Fraction(8, 9) * (Fraction(1, 1) - Fraction(1, 4**(k-1)))

print("=" * 60)
print("NS 高层展开: S(ω^k) 和 S(ω^ω)")
print("=" * 60)
print()

for k in range(1, 7):
    s = S_omega_pow(k)
    print(f"  S(ω^{k}) = {float(s):.8f} = {s}")

print(f"  ...")
print(f"  S(ω^ω) = lim S(ω^k) = 1 + 8/9 = 17/9 ≈ {float(Fraction(17,9)):.8f}")

print()
print("=" * 60)
print("完整展开视图")
print("=" * 60)
print("""
  S(1)   = 0
  S(ω)   = 1                                      [块 0: +1/2 +1/4 +1/8...]
  
  S(ω·2) = 1 + 1/2                                [块 1: +1/4 +1/8 +1/16...]
  S(ω·3) = 1 + 1/2 + 1/8                          [块 2: +1/16 +1/32...]
  S(ω·4) = 1 + 1/2 + 1/8 + 1/32                   [块 3: +1/64 +1/128...]
  S(ω²)  = 1 + 1/2 + 1/8 + 1/32 + 1/128 + ...    [极限]
         = 5/3 ≈ 1.667
  
  S(ω²+ω)   = 5/3 + 1/8 = 43/24                   [块: +1/16 +1/32...]
  S(ω²+ω·2) = 43/24 + 1/32 = 175/96               [块: +1/64 +1/128...]
  S(ω²+ω·3) = 175/96 + 1/128
  S(ω²·2)   = 43/24 + 1/32 + 1/128 + ...          [极限]
            = 11/6 ≈ 1.833
  
  S(ω³)     = ... (更深的嵌套)
  S(ω^k)    = 1 + (8/9)(1 - 1/4^{k-1})
  S(ω^ω)    = 1 + 8/9 = 17/9 ≈ 1.889
""")

print("=" * 60)
print("结构洞察")
print("=" * 60)
print("""
1. 每个 ω^k 层级引入自己的 "内嵌几何级数树"
2. ω^k 层的总贡献 = 8/(3·4^k)
3. S 值始终 < 2: 所有层贡献之和 = Σ 8/(3·4^k) = (8/3)/(3) = 8/9 < 1
4. 所以 sup S(α) ≤ 1 + 8/9 = 17/9 (在 ω^ω 达到)
5. f(ω^k) = 4^k: 每升一级 ω，f 翻 4 倍
6. 在 ω^k·m 处，f = 4^{k+m-1}
""")
