"""
BM4 → NS 实数对应表构建器

一一对应: 每个 BM4 标准极限表达式对应一个 NS 实数 S 值。
基于基本列结构对比来确定对应关系。

BM4 结构 → 序数 → NS S 值:
  (0)(1): FS = (0)^n 线性 → ω → S=1
  (0)(1)(1): FS = (0)(1) 周期 → ω·2 → S=1.5
  (0)(1)(2): FS = (0)(1)(1)^n → ω^ω → S≈1.844
  (0)(1,1): FS = (0)(1)(2)(3)... 连续递推 → ε₀ → S≈?
"""

from fractions import Fraction
from ns_engine import f_of, expand

def S_to_str(s: Fraction) -> str:
    """将 S 值格式化为分数 + 小数"""
    return f"{s} ≈ {float(s):.6f}"

# ============================================================
# 已确认的对应关系
# ============================================================

# S 值精确计算
S_vals = {
    '1':   Fraction(0, 1),      # S(1) = 0
    '2':   Fraction(1, 2),      # S(2) = 1/2
    '3':   Fraction(3, 4),      # S(3) = 3/4
    'w':   Fraction(1, 1),      # S(ω) = 1
    'w+1': Fraction(5, 4),      # S(ω+1) = 5/4
    'w+2': Fraction(11, 8),     # S(ω+2) = 11/8
    'w*2': Fraction(3, 2),      # S(ω·2) = 3/2
    'w*3': Fraction(13, 8),     # S(ω·3) = 13/8
    'w^2': Fraction(5, 3),      # S(ω²) = 5/3
    'w^3': Fraction(11, 6),     # S(ω³) = 11/6
    'w^4': Fraction(59, 32),     # S(ω⁴) ≈ 1.84375
}

# S(ω^ω) 通过级数极限计算
# f(ω^k) = 2^{2^k}, 贡献 = 8/(3·2^{2^k})
S_ww = Fraction(1, 1)
for k in range(1, 8):
    fk = 2 ** (2 ** k)
    S_ww = S_ww + Fraction(8, 3 * fk)

# ============================================================
# 核心: f(ε₀) = 4，因为 ε₀[2] = ω
# 这意味 ε₀ 层产生与 ω 层相同的 2/3 推力
# ============================================================

f_e0 = f_of('e0', {})
print(f"f(ε₀) = f(ω) = {f_e0}")
print(f"S(ω^ω) = {float(S_ww):.10f} = {S_ww}")

# S(ε₀) = S(ω^ω) + (ω^ω→ε₀ 之间的贡献)
# ω^ω→ε₀ 的路径: ω^ω → ω^ω^ω → ω^ω^ω^ω → ... → ε₀
# 每级贡献极小 (f 双指数)
# 近似: S(ε₀) ≈ S(ω^ω) + very_small
S_e0_approx = S_ww
print(f"S(ε₀) ≈ S(ω^ω) ≈ {float(S_e0_approx):.10f}")
print(f"  (ε₀ 之前 f 值极大，附加贡献 < 10^-6)")

# ε₀ 之后: f(ε₀)=4 → 贡献 2/3
S_e0_w = S_e0_approx + Fraction(2, 3)
print(f"S(ε₀·2) ≈ S(ε₀) + 2/3 ≈ {float(S_e0_w):.10f}")

# ============================================================
# 对应表
# ============================================================

print()
print("=" * 80)
print("  BM4 → NS 实数一一对应表")
print("=" * 80)

table = [
    # (BM4表达式, 对应序数, NS S值, 备注)
    ("(0)",         "1",     Fraction(0, 1),      "起点"),
    ("(0)(0)",      "2",     Fraction(1, 2),      "后继"),
    ("(0)(0)(0)",   "3",     Fraction(3, 4),      "后继"),
    ("",            "",      None,                ""),
    ("(0)(1)",      "ω",     Fraction(1, 1),      "FS: (0)^n 线性"),
    ("(0)(1)(0)",   "ω+1",   Fraction(5, 4),      "后继"),
    ("(0)(1)(1)",   "ω·2",   Fraction(3, 2),      "FS: (0)(1) 周期重复"),
    ("(0)(1)(1)(1)","ω·3",   Fraction(13, 8),     "FS: (0)(1)(1) 周期重复"),
    ("",            "",      None,                ""),
    ("(0)(1)(2)",   "ω^ω",   S_ww,                "FS: (0)(1)(1)^n"),
    ("(0)(1)(2)(1)","ω^(ω+1)",None,               "FS: (0)(1)(2) 块重复"),
    ("(0)(1)(2)(3)","ω^ω^ω", None,                "FS: (0)(1)(2)(2)^n"),
    ("",            "",      None,                ""),
    ("(0)(1,1)",    "ε₀",    S_e0_approx,         "f=4 复位! FS: 连续递增"),
    ("(0)(1,1)(2)", "ε₀·ω",  None,                "FS: (0)(1,1)(1,1)^n"),
    ("(0)(1,1)(2,1)","ε_ω? ζ₀?",None,             "FS: 交错模式"),
    ("(0)(1,1,1)",  "φ_ω(0)",None,                "FS: 同(0)(1,1)头部"),
    ("",            "",      None,                ""),
    ("(0,0)(1,1)",  "Γ₀",    None,                "=(0)(1,1) in BM4!"),
]

print(f"{'BM4':<18s} {'NS序数':<12s} {'NS S值':<30s} {'备注':<20s}")
print("-" * 80)
for bm4, ord_name, s_val, note in table:
    if not bm4:
        print()
        continue
    s_str = S_to_str(s_val) if s_val else "[待计算]"
    print(f"{bm4:<18s} {ord_name:<12s} {s_str:<30s} {note:<20s}")

print()
print("=" * 80)
print("  已确认值 (精确分数)")
print("=" * 80)
confirmed = [
    ("(0) → 1",     "S(1)=0"),
    ("(0)(0) → 2",  "S(2)=1/2"),
    ("(0)(1) → ω",  "S(ω)=1"),
    ("(0)(1)(1) → ω·2", "S(ω·2)=3/2"),
    ("(0)(1)(2) → ω^ω", f"S(ω^ω)={S_ww}"),
    ("(0)(1,1) → ε₀", f"f(ε₀)=4, S≈{float(S_e0_approx):.6f}"),
]
for left, right in confirmed:
    print(f"  {left:<20s} : {right}")

print()
print("关键发现:")
print(f"  f(ε₀) = f(ω) = {f_e0} — ε₀ 处 f 复位到 ω 级别!")
print(f"  这意味着 ε₀ 后产生 +2/3 的大推力 (与 ω→ω² 同级)")
print(f"  S(ε₀·2) ≈ S(ε₀) + 2/3 ≈ {float(S_e0_w):.6f}")
