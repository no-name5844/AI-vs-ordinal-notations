"""
NS 继续推高: ε₀ → ε₁ → ... 级联
使用指数上标的加权分数和表示

形式: S(α) = 基准 + 8/3 · Σ 1/2^{指数}

规则:
  f(λ) = f(expand(λ, 2))
  每层贡献 = 8/(3·f(λ)) = 8/(3·2^{log₂ f(λ)})
  
  f 值追踪:
  f(ω)      = 2²  = 4
  f(ω²)     = 2⁴  = 16
  f(ω³)     = 2⁸  = 256
  f(ω^k)    = 2^{2^k}
  f(ω^ω)    = 2⁴  = 16   (复位)
  f(ε₀)     = 2²  = 4    (完全复位!)
  f(ω^(ε₀+1)) = 2⁴ = 16
  f(ε₁)     = 2⁴  = 16   (不完全复位)
  f(ω^(ε₁+1)) = f(ε₁·2) = f(ε₁+ε₁[2]) = f(ε₁+ω) = f(ε₁)·4 = 2⁶? or different?

关键: 对于 ε_{α+1}, ε_{α+1}[n] 的基本列:
  ε_{α+1}[0] = ε_α+1
  ε_{α+1}[n+1] = ω^{ε_{α+1}[n]}
  
  所以 ε_{α+1}[2] = ω^{ε_α+1}
  
  f(ε_{α+1}) = f(ω^{ε_α+1})

对于 α=0 (ε₁):
  f(ε₁) = f(ω^(ε₀+1)) = f(ε₀·2) = f(ε₀)·4 = 4·4 = 16

对于 α=1 (ε₂):
  f(ε₂) = f(ω^(ε₁+1)) = f(ε₁·2) = f(ε₁)·4 = 16·4 = 64

对于 α=α (ε_{α+1}):
  f(ε_{α+1}) = f(ε_α)·4 = 4·4^{α+1}??
  
  起始: f(ε₀)=4=2²
  f(ε₁) = f(ε₀)·4 = 16 = 2⁴
  f(ε₂) = f(ε₁)·4 = 64 = 2⁶
  f(ε₃) = f(ε₂)·4 = 256 = 2⁸
  
  f(ε_k) = 4^{k+1}? 检查: f(ε₀)=4=4¹, f(ε₁)=16=4², f(ε₂)=64=4³ ✓
  f(ε_k) = 2^{2(k+1)}.

所以:
  ε₀ 层贡献 = 8/(3·2²) = 8/12 = 2/3
  ε₁ 层贡献 = 8/(3·2⁴) = 8/48 = 1/6
  ε₂ 层贡献 = 8/(3·2⁶) = 8/192 = 1/24
  ε₃ 层贡献 = 8/(3·2⁸) = 8/768 = 1/96
  ...

  ε_k 层贡献 = 8/(3·4^{k+1}) = (2/3)·(1/4)^k

这意味着 ε 层级联是几何级数!
"""

from fractions import Fraction

print("=" * 70)
print("NS: ε₀ → ε₁ → ε₂ → ... 级联")
print("使用指数上标加权分数和")
print("=" * 70)

# S(ε₀) = S(ω^ω) 近似
# S(ω^ω) = 1 + 8/3·(1/2² + 1/2⁴ + 1/2⁸ + 1/2¹⁶ + ...)
#        ≈ 1.84379

S_w_omega = Fraction(1, 1)
for k in range(1, 8):
    S_w_omega += Fraction(8, 3 * 2**(2**k))

print(f"""
已知:
  S(1) = 0
  S(ω) = 1
  S(ω^ω) = 1 + 8/3·(1/2² + 1/2⁴ + 1/2⁸ + 1/2¹⁶ + ...)
         ≈ {float(S_w_omega):.6f}

  f(ε₀) = 2² = 4   (完全复位到 ω 级别)
  f(ε₁) = 2⁴ = 16
  f(ε₂) = 2⁶ = 64
  f(ε₃) = 2⁸ = 256
  
  一般: f(ε_k) = 4^(k+1) = 2^(2(k+1))
""")

print("=" * 70)
print("ε 层级联: 每层贡献 = 8/(3·4^{k+1}) = (2/3)·(1/4)^k")
print("=" * 70)

# ω^ω → ε₀: 中间有 ω^ω^ω, ω^ω^ω^ω, ...
# f(ω^ω^ω) = f(ω^(ω²)) = ...
# 这些中间层 f 极大 (双指数)，贡献可忽略
# S(ε₀) ≈ S(ω^ω)

S = S_w_omega  # S(ε₀) 近似
print(f"\nS(ε₀) ≈ S(ω^ω) ≈ {float(S):.10f}")

print(f"\n{'层级':<10s} {'f 值':<15s} {'贡献':<25s} {'累积 S':<20s}")
print("-" * 70)

for k in range(0, 8):
    f_val = 4 ** (k+1)  # f(ε_k)
    contrib = Fraction(8, 3 * f_val)
    S = S + contrib
    exponent = 2 * (k+1)
    cont_str = f"8/(3·2^{exponent}) = 8/(3·{2**exponent})"
    print(f"ε_{k}→ε_{k+1}  f=2^{exponent:<3d}  {cont_str:<25s}  ≈ {float(S):.10f}")
    
    if float(contrib) < 1e-8:
        print(f"  ... (贡献 < 10^-8, 截断)")
        break

# 极限: S(ε_ω) = S + (2/3)·(1/(1-1/4) - 1)?
# ε 层级联 = (2/3)·(1 + 1/4 + 1/16 + ...)
# = (2/3)/(1-1/4) = (2/3)/(3/4) = 8/9

S_epsilon_omega = S_w_omega + Fraction(8, 9)
print(f"\nS(ε_ω) ≈ S(ω^ω) + 8/9 ≈ {float(S_epsilon_omega):.10f}")

print("""
══════════════════════════════════════════════════════════════════
  NS ε 层级联总结
══════════════════════════════════════════════════════════════════

  S(ε_ω) = S(ω^ω) + 8/3 · Σ_{k=0}^{∞} 1/4^{k+1}
         = S(ω^ω) + 8/3 · (1/4)/(1-1/4)
         = S(ω^ω) + 8/3 · 1/3
         = S(ω^ω) + 8/9
         
         ≈ 1.844 + 0.889 = 2.733

  ε 层级联是几何级数! 比例 1/4。
  
  继续:
  S(ε_{ω·2}) = S(ε_ω) + ... (ε_{ω+1}, ε_{ω+2}, ... 类似)

  下一个大级别: ζ₀ = φ(2,0)
  f(ζ₀) = ? (需要 Veblen 函数基本列)
""")
