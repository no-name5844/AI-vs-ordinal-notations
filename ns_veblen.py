"""
NS Veblen 层级联 — 嵌套分形结构

已确认的 f 值:
  f(ω)   = 2^2 = 4
  f(ω^ω) = 2^4 = 16
  f(ε₀)  = 2^2 = 4    ← 完全复位!
  f(ζ₀)  = 2^6 = 64

结构:
  ζ₀ = lim{ε_0, ε_{ε_0}, ε_{ε_{ε_0}}, ...}
  ζ₀[1] = ε₀, ζ₀[2] = ε_{ε₀}, ζ₀[3] = ε_{ε_{ε₀}}, ...

  f(ζ₀) = f(ε_{ε₀}) = f(ε_ω) = f(ε_2) = 64

关键: ζ 层级联中的 S 值:
  S(ε₀)     ≈ 1.844
  S(ε_ω)    ≈ 2.733  (ε 级联 +8/9)
  S(ε_{ε₀}) > 2.733  (ε₀<ε₀, 又是一层)
  
  每一层 ε_{...ε₀} 到 ε_{...ε_{ε₀}} 都产生一次级联
  
  这形成了嵌套分形: 
    S(ζ₀) = S(ε₀) + cascade(ε₀→ε_{ε₀}) + cascade(ε_{ε₀}→ε_{ε_{ε₀}}) + ...
    
  每个子级联的大小取决于 f 在该级别的值
"""

from fractions import Fraction

print("=" * 70)
print("NS Veblen 层级联 — 嵌套结构分析")
print("=" * 70)

# 已确认值
S_e0 = Fraction(1, 1)
for k in range(1, 8):
    S_e0 += Fraction(8, 3 * 2**(2**k))

# ε 级联贡献
S_ew = S_e0 + Fraction(8, 9)

print(f"""
已确认:
  S(ω^ω) ≈ {float(S_e0):.6f}
  
  ε 级联: f(ε_k) = 2^{{2(k+1)}}, 贡献 = Σ 8/(3·2^{{2(k+1)}})
  S(ε_ω) = S(ω^ω) + 8/9 ≈ {float(S_ew):.6f}
  
  ζ₀ 的结构:
    ζ₀ = lim{{ε₀, ε_{{ε₀}}, ε_{{ε_{{ε₀}}}}, ...}}
    ζ₀[1]=ε₀, ζ₀[2]=ε_{{ε₀}}, ...
    
  f(ζ₀) = f(ε_{{ε₀}}) = f(ε_ω) = f(ε_2) = 2^6 = 64
""")

# ζ 层级内联
# 路径: ε₀ → ε₁ → ... → ε_ω → ε_{ω+1} → ... → ε_{ε₀}
#        → ε_{ε₀+1} → ... → ε_{ε_ω} → ... → ε_{ε_{ε₀}} → ...
#        → ζ₀

print("""
ζ₀ 到 ζ¹ (即 φ(2,0)→φ(2,1)) 的贡献:
  
  与 ε₀ 层类比:
  - ε₀ 层: f(ε₀)=4, 贡献=2/3, f(ε₁)=16, 贡献=1/6, ...
  - ζ₀ 层: f(ζ₀)=64, 贡献=1/24, f(ζ₁)=2^?=256, 贡献=1/96, ...
  
  等比: 1/24, 1/96, 1/384, ... (公比 1/4)
  
  ζ_ω = lim ζ_k:
  Σ 1/(24·4^k) = (1/24)/(1-1/4) = (1/24)·(4/3) = 1/18

  S(ζ_ω) = S(ζ₀) + 1/18
""")

# 构建 ζ₀ 到 ζ_ω 的级联
print("=" * 70)
print("ζ 级联 (φ(2,0)→φ(2,ω))")
print("=" * 70)

S_zeta = Fraction(0, 1)
print(f"{'层':<12s} {'f':<12s} {'贡献':<25s} {'累积':<15s}")
print("-" * 65)

# 从 ζ₀ 开始 (近似 S(ζ₀) = S(ε_ω) + 一些)
# 由于 ε_ω < ζ₀，S(ζ₀) > S(ε_ω)
# S(ζ₀) 准确值需要计算 ε_ω→ζ₀ 之间的嵌套级联
# 近似: S(ζ₀) ≈ S(ε_ω) + 小量 (f 很大)

for k in range(0, 8):
    f_val = 64 * (4 ** k)
    contrib = Fraction(8, 3 * f_val)
    S_zeta += contrib
    exp = 6 + 2*k
    print(f"ζ_{k}→ζ_{k+1}   f=2^{exp:<3d}  8/(3·2^{exp}) = 8/(3·{2**exp})    ≈ {float(S_zeta):.10f}")
    if float(contrib) < 1e-8:
        break

print(f"\nζ 级联总贡献 = 1/18 ≈ {float(Fraction(1,18)):.10f}")
print(f"  (需要加上 S(ζ₀) 的基准值)")

# η₀ = φ(3,0)
print("""
══════════════════════════════════════════════════════════════
  η₀ = φ(3,0) — 下一个 Veblen 级别
══════════════════════════════════════════════════════════════

  η₀[1] = ζ₀, η₀[2] = φ(2, ζ₀)

  f(η₀) = f(φ(2, ζ₀)) = f(φ(2, ζ₀[2])) = f(φ(2, ε_{ε₀}))
  = f(φ(2, ε_ω)) = f(φ(2, ε_2))

  ...经过深层递归... = f(ζ_{某些大序数}) = 2^{很大}

  猜测: f(η₀) = f(ζ_2) 类似 f(ζ₀)=f(ε_2) 的模式
  
  f(ζ₀) = f(ε_2) = 2^6
  类推: f(η₀) = f(ζ_2) = f(ζ₁)·4? 需要验证
  
  ζ₁: 需要计算。但模式类似。
""")

# Veblen 层级总结
print("=" * 70)
print("Veblen 层级贡献推测 (需验证)")
print("=" * 70)
print(f"""
  ω 层:      S(ω^ω) - S(ω) = Σ 8/(3·2^{{2^k}}) ≈ 0.844
  ε 级联:    S(ε_ω) - S(ε₀) = 8/9              ≈ 0.889  
  ζ 级联:    S(ζ_ω) - S(ζ₀) = 1/18             ≈ 0.056
  η 级联:    (推测约 1/72)                     ≈ 0.014
  ...
  
  各项 ≈ (2/3)·(1/4)^{{k-1}} 几何衰减?
  
  如果 Veblen 级联也是公比 1/4:
  总贡献 = (2/3)/(1-1/4) = 8/9
  
  那么 S(Γ₀) ≈ S(ω^ω) + 8/9 ≈ 1.844 + 0.889 = 2.733
  
  但这似乎太小了——每个 Veblen level 应该叠加。
  
  实际上:
  - ε cascade: S(ε_ω) ≈ 2.733
  - ζ cascade: adds ~0.056, S(ζ_ω) ≈ S(ζ₀) + 0.056
  - η cascade: adds ~0.014
  - Total: ≈ S(ε_ω) + 各种嵌套 + 这些级联 ≈ 2.8+
  
  这个嵌套分形结构需要更精确的计算!
""")
